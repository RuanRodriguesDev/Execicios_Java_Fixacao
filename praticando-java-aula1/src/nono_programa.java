public class nono_programa {
    public static void main(String[] args) {
        // Declaração das variáveis
        char letra = 13012;       // Declara uma variável do tipo char e inicializa com um valor inteiro
        String frase = "Meu nome começa com a letra ";    // Declara uma variável do tipo String e inicializa com um valor de texto
        String resultado = frase + letra;  // Concatena a String frase com o char letra e armazena em resultado

        // Exibe o resultado da concatenação
        System.out.println("O resultado: " + resultado);
    }
}
